function resolveLocale(preferred) {
  if (preferred && LOCALES[preferred]) return preferred;
  if (preferred) {
    const short = String(preferred).split(/[-_]/)[0];
    const match = LOCALE_ORDER.find((code) => code === short || code.startsWith(`${short}-`));
    if (match) return match;
  }
  const nav = (typeof navigator !== "undefined" && navigator.language) || "";
  if (LOCALES[nav]) return nav;
  const navShort = nav.split(/[-_]/)[0];
  const matchNav = LOCALE_ORDER.find((code) => code === navShort || code.startsWith(`${navShort}-`));
  return matchNav || "en";
}

function applyLocale(locale) {
  const dict = STRINGS[locale] || null;
  document.documentElement.setAttribute("lang", locale);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (!key) return;
    if (!dict) {
      // English: HTML already has the default text; no-op unless it's been
      // overwritten by a previous non-en run, which applyLocale handles by
      // re-reading the original text — for simplicity we keep the default.
      return;
    }
    const value = dict[key];
    if (typeof value === "string") el.textContent = value;
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (!key) return;
    if (!dict) return;
    const value = dict[key];
    if (typeof value === "string") el.innerHTML = value;
  });

  const label = document.getElementById("langSwitcherLabel");
  if (label) label.textContent = LOCALES[locale]?.name || locale;

  // Re-render year (might have been in an i18n-html block).
  const year = document.getElementById("year");
  if (year) year.textContent = new Date().getFullYear();

  // Mark current locale in the menu.
  document.querySelectorAll("#langSwitcherMenu li").forEach((li) => {
    li.classList.toggle("is-active", li.dataset.locale === locale);
  });
}

function saveLocale(locale) {
  try {
    localStorage.setItem(STORAGE_KEY, locale);
  } catch (_) {
    // ignore
  }
}

function readSavedLocale() {
  try {
    return localStorage.getItem(STORAGE_KEY) || null;
  } catch (_) {
    return null;
  }
}

// Capture the English defaults from the HTML so switching back to "en" works.
function captureDefaults() {
  const defaults = { text: new Map(), html: new Map() };
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    defaults.text.set(el, el.textContent);
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    defaults.html.set(el, el.innerHTML);
  });
  return defaults;
}

function restoreDefaults(defaults) {
  defaults.text.forEach((v, el) => { el.textContent = v; });
  defaults.html.forEach((v, el) => { el.innerHTML = v; });
}

function buildLanguageMenu(onSelect) {
  const menu = document.getElementById("langSwitcherMenu");
  if (!menu) return;
  menu.replaceChildren();
  for (const code of LOCALE_ORDER) {
    const meta = LOCALES[code];
    if (!meta) continue;
    const li = document.createElement("li");
    li.dataset.locale = code;
    li.setAttribute("role", "menuitemradio");
    const name = document.createElement("span");
    name.className = "lang-switcher__name";
    name.textContent = meta.name;
    const codeEl = document.createElement("span");
    codeEl.className = "lang-switcher__code";
    codeEl.textContent = code;
    li.append(name, codeEl);
    li.addEventListener("click", () => onSelect(code));
    menu.append(li);
  }
}

document.addEventListener("DOMContentLoaded", () => {
  const defaults = captureDefaults();
  const current = { locale: resolveLocale(readSavedLocale()) };

  buildLanguageMenu((code) => {
    if (code === current.locale) {
      toggleMenu(false);
      return;
    }
    current.locale = code;
    saveLocale(code);
    restoreDefaults(defaults);
    if (code !== "en") applyLocale(code);
    applyLocale(code); // also updates the label/active marker, no-op for text if en
    toggleMenu(false);
  });

  // Initial apply
  if (current.locale !== "en") applyLocale(current.locale);
  applyLocale(current.locale);

  const button = document.getElementById("langSwitcherButton");
  const menu = document.getElementById("langSwitcherMenu");
  function toggleMenu(force) {
    const shouldOpen = typeof force === "boolean" ? force : menu.hasAttribute("hidden");
    if (shouldOpen) {
      menu.removeAttribute("hidden");
      button?.setAttribute("aria-expanded", "true");
    } else {
      menu.setAttribute("hidden", "");
      button?.setAttribute("aria-expanded", "false");
    }
  }
  button?.addEventListener("click", (event) => {
    event.stopPropagation();
    toggleMenu();
  });
  document.addEventListener("click", (event) => {
    if (!menu.hasAttribute("hidden") && !menu.contains(event.target) && event.target !== button) {
      toggleMenu(false);
    }
  });
  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") toggleMenu(false);
  });

  const year = document.getElementById("year");
  if (year) year.textContent = new Date().getFullYear();
});
